    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     int filaSeleccionada = TableCompra.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(null, "Selecciona una fila para eliminar.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(null, "¬øEst√°s seguro de que deseas eliminar este registro?", "Confirmar Eliminaci√≥n", JOptionPane.YES_NO_OPTION);
    if (confirmacion == JOptionPane.YES_OPTION) {
        // Eliminar la fila seleccionada del modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) TableCompra.getModel();
        modelo.removeRow(filaSeleccionada); // Elimina la fila

        JOptionPane.showMessageDialog(null, "Registro eliminado correctamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void TxtIdPedidoVentaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtIdPedidoVentaKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
            String inputText = TxtIdPedidoVenta.getText().trim();

            if (inputText.isEmpty()) {
                //cargarTabla();
            } else {
                try {
                    int ropaId = Integer.parseInt(inputText);

                    Optional<DetallePedidoCompra> ropaOpt = detallepedidoCompraController.obtenerdetalle(ropaId);

                    ropaOpt.ifPresentOrElse(ropa -> {
                        String[] columnNames = {"ID", "Cantidad", "Precio", "Ropa"};
                        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

                        model.addRow(new Object[]{
                            ropa.getId(),
                            ropa.getCantidad(),
                            ropa.getPrecio(),
                            ropa.getIdRopa()
                        });

                        TableCompra.setModel(model);
                    }, () -> {
                        JOptionPane.showMessageDialog(null, "Pedido no encontrado");
                    });
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Por favor, ingrese un ID v√°lido");
                }
            }
        }
    }//GEN-LAST:event_TxtIdPedidoVentaKeyPressed
    
    private void cargarTabla() {
        List<DetallePedidoCompra> tiendaList = detallepedidoCompraController.listarPedidoCompra();  // Get the list of Tiendas

        if (tiendaList.isEmpty()) {
            return;
        }

        String[] columnNames = {"Cantidad", "Ropa", "Fecha de Modificaci√≥n"};

        // Create the table model
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        // Iterate over the list of Tiendas and add rows to the table model
        for (DetallePedidoCompra tienda : tiendaList) {
            model.addRow(new Object[]{
                    tienda.getCantidad(),
                    tienda.getIdRopa(),
                    tienda.getFechaUltModificacion()
            });
        }

        // Set the table model to jTable1
        TableCompra.setModel(model);
    }
