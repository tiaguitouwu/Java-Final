    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           
          // Validaci√≥n del campo Usuario
    String usuario = TxtUsuario.getText().trim();
    if (usuario.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El campo Usuario no puede estar vac√≠o.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validaci√≥n del campo N√∫mero de Cliente
    String nroCliente = TxtNroCliente.getText().trim();
            if (nroCliente.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El campo N√∫mero de Cliente no puede estar vac√≠o.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Validar y obtener los datos de los campos restantes
        int idRopa = Integer.parseInt(TxtId.getText().trim());
        String articulo = TxtArticulo.getText().trim();

        // Validaci√≥n de precio
        int precio = Integer.parseInt(TxtPrecio.getText().trim());
        if (precio <= 0) {
            JOptionPane.showMessageDialog(null, "El precio debe ser mayor a cero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validaci√≥n de cantidad
        int cantidad = Integer.parseInt(TxtCantidad.getText().trim());
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser mayor a cero.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // C√°lculo del subtotal
        int subtotal = cantidad * precio;

        // Agregar los datos a la tabla
        DefaultTableModel modelo = (DefaultTableModel) TableCompra.getModel();
        modelo.addRow(new Object[]{idRopa, articulo, precio, cantidad, subtotal, nroCliente});

        // Limpiar los campos despu√©s de guardar
        TxtUsuario.setText("");
        TxtId.setText("");
        TxtArticulo.setText("");
        TxtPrecio.setText("");
        TxtCantidad.setText("");
        TxtNroCliente.setText("");

        // Mostrar mensaje de √©xito
        JOptionPane.showMessageDialog(null, "Datos procesados correctamente:\n"
            + "Usuario: " + usuario + "\n"
            + "N√∫mero de Cliente: " + nroCliente + "\n"
            + "Art√≠culo: " + articulo + "\n"
            + "Cantidad: " + cantidad + "\n"
            + "Precio: " + precio + "\n"
            + "Subtotal: " + subtotal, "√âxito", JOptionPane.INFORMATION_MESSAGE);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, ingrese valores num√©ricos v√°lidos en los campos ID, Precio y Cantidad.", "Error", JOptionPane.ERROR_MESSAGE);
    }
           
                    
    }//GEN-LAST:event_jButton1ActionPerformed

