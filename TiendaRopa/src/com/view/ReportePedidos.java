    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
        String inputText = jTextField1.getText().trim();
        
            if (inputText.isEmpty()) {
                cargarTabla();
            } else {
                try {
                    int pedidoId = Integer.parseInt(inputText); 

                    Optional<PedidoCompra> pedidoOpt = pedidoCompraController.obtenerPedidoCompra(pedidoId);
                    pedidoOpt.ifPresentOrElse(pedido -> {
                    String[] columnNames = {"ID", "Nro. Pedido", "ID Proveedor", "ID Tienda", "Fecha Pedido", "Estado", "Usuario", "Fecha Mod."};

                    DefaultTableModel model = new DefaultTableModel(columnNames, 0);

                    model.addRow(new Object[]{
                        pedido.getId(),
                        pedido.getNroPedido(),
                        pedido.getIdProveedor(),
                        pedido.getIdTienda(),
                        pedido.getFechaPedido(),
                        pedido.getEstado(),
                        pedido.getUsuario(),
                        pedido.getFechaUltModificacion()
                    });

                        jTable1.setModel(model);
                    }, () -> {
                        JOptionPane.showMessageDialog(null, "Pedido no encontrado");
                    });
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Por favor, ingrese un ID v√°lido");
                }
            }
        }
    }

        public void cargarTabla(){
        List<PedidoCompra> pedidoCompraList = pedidoCompraController.listarPedidoCompra();
        if (pedidoCompraList.isEmpty()) {
            return;
        }
        String[] columnNames = {"ID", "Nro. Pedido", "ID Proveedor", "ID Tienda", "Fecha Pedido", "Estado", "Usuario", "Fecha Mod."};

        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        for (PedidoCompra pedido : pedidoCompraList) {
            model.addRow(new Object[]{
                        pedido.getId(),
                        pedido.getNroPedido(),
                        pedido.getIdProveedor(),
                        pedido.getIdTienda(),
                        pedido.getFechaPedido(),
                        pedido.getEstado(),
                        pedido.getUsuario(),
                        pedido.getFechaUltModificacion()
                    });
        }

        // Set the table model to jTable1
        jTable1.setModel(model);
    }//GEN-LAST:event_jTextField1KeyPressed

